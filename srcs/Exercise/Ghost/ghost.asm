 clearScreen:
   mov ax, 0xA000
   mov es, ax
   mov di, 0
   mov cx, 200*320
   rep stosb
   ret

; si most have the sprite adress
; di most have the target adress
draw_ghost:
    mov ax, 0xA000
    mov es, ax
    mov dx, 8
    .eachLine:
        mov cx, 8
        rep movsb
        add di, 320-8
        dec dx
        jnz .eachLine
        ret

; Change the color of the ghost when it touches the wall 
changeSprite:
    jg .clyde
    mov si, clyde
    .clyde:
    ret

; -----------------------------------------------
; Wait for vertical sync.
; scraps ax and dx
gfxWaitForVSync:
   mov dx, 0x3DA
.loop1:
   in al, dx
   test al, 8
   jnz .loop1
.loop2:
   in al, dx
   test al, 8
   jz .loop2
   ret

presentBackBuffer:
